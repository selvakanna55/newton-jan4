import java.lang.Math;
import java.util.Scanner;

class Loops {
    public static void main(String[] args) {
        int a = 10000;
        System.out.println(a);
        a = a+1;
        System.out.println(a);
        a++; // a = a+1  is same as  a++: post increment
        //++a  pre increment  by 1
        // number = a++;
        // number = ++a;
        System.out.println(a);
    }
}
